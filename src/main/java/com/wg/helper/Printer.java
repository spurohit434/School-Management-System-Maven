package com.wg.helper;

public class Printer<T> {
	public void print(T obj) {
		System.out.println(
				"+---------------+----------+------------+---------------+--------+----------+---------+---------+------+---------+---------+---------------+---------------+---------------+----------+------------+---------------+--------+----------+---------+---------+------+---------+---------+");
		System.out.println(obj.toString().replace(",", " |").replace("User{", "").replace("}", ""));
		System.out.println(
				"+---------------+----------+------------+---------------+--------+----------+---------+---------+------+---------+---------+---------------+---------------+---------------+----------+------------+---------------+--------+----------+---------+---------+------+---------+---------+");
	}
}